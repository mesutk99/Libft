#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	i;

	if (!s)
		return ;
	i = 0;
	while (s[i] && !(s == NULL))
	{
		(*f)(i, &s[i]);
		i++;
	}
}
